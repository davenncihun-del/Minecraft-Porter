def download_file(token: str):
    """Strictly validates download tokens before serving binaries."""
    if not re.match(r"^[a-f0-9]{32}$", token): raise HTTPException(status_code=400, detail="Invalid token format.")
    downloadable = DOWNLOAD_FILES.get(token)
    if not downloadable or not downloadable.path.exists():
        raise HTTPException(status_code=404, detail="Artifact not found or expired from cache.")
