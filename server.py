# --- Core Utilities & Memory Sweepers ---
def sanitize_filename(filename: str) -> str:
    """Strictly prevents directory traversal exploits from malicious client payloads."""
    return Path(filename).name

def version_tuple(version: str):
    parts = []
    for part in version.split('.'):
        digits = re.match(r"(\d+)", part)
        parts.append(int(digits.group(1)) if digits else 0)
    return tuple(parts)

def cleanup_old_files():
    """Non-blocking background garbage collector to prevent memory leaks."""
    cutoff = datetime.utcnow() - timedelta(minutes=10)
    
    for fid in [k for k, v in STORED_FILES.items() if v.created_at < cutoff]:
        try: STORED_FILES[fid].path.unlink(missing_ok=True)
        except OSError: pass
        STORED_FILES.pop(fid, None)

    for tok in [k for k, v in DOWNLOAD_FILES.items() if v.created_at < cutoff]:
        try: DOWNLOAD_FILES[tok].path.unlink(missing_ok=True)
        except OSError: pass
        DOWNLOAD_FILES.pop(tok, None)
        
    for tid in [k for k, v in TASKS_STORE.items() if v.get("created_at", datetime.utcnow()) < cutoff]:
        TASKS_STORE.pop(tid, None)


