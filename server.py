def detect_loader_and_metadata_in_zip(archive: zipfile.ZipFile):
    names = [n.lower() for n in archive.namelist()]
    if "fabric.mod.json" in names: return "Fabric", archive.namelist()[names.index("fabric.mod.json")]
    if "meta-inf/mods.toml" in names: return "Forge", archive.namelist()[names.index("meta-inf/mods.toml")]
    if "meta-inf/neoforge.mods.toml" in names: return "NeoForge", archive.namelist()[names.index("meta-inf/neoforge.mods.toml")]
    if "pack.mcmeta" in names: return "Vanilla", archive.namelist()[names.index("pack.mcmeta")]
    return "Unknown", None

def detect_loader_and_metadata(path: Path):
    with zipfile.ZipFile(path, "r") as archive:
        return detect_loader_and_metadata_in_zip(archive)

def _clean_version_string(raw: str) -> Optional[str]:
    if not raw or not raw.strip(): return None
    cleaned = raw.strip()
    cleaned = re.sub(r'[\[\](){}]', '', cleaned)
    if ',' in cleaned: cleaned = [p.strip() for p in cleaned.split(',') if p.strip()][0]
    cleaned = re.sub(r'^[><=~^]+\s*', '', cleaned).strip()
    return cleaned if cleaned and cleaned[0].isdigit() else None

def extract_current_version(loader: str, metadata_path: str, text: str) -> Optional[str]:
    if loader == "Fabric" or loader == "Vanilla":
        try: data = json.loads(text)
        except json.JSONDecodeError: data = {}
        if loader == "Fabric":
            depends = data.get("depends", {})
            if isinstance(depends, dict) and depends.get("minecraft"):
                return _clean_version_string(depends["minecraft"])
            if data.get("minecraft_version"):
                return _clean_version_string(data["minecraft_version"])
        if loader == "Vanilla":
            pack = data.get("pack", {})
            for k in ["supported_minecraft_version", "minecraft_version"]:
                if isinstance(pack, dict) and pack.get(k): return _clean_version_string(pack[k])
                if data.get(k): return _clean_version_string(data[k])
    
    if loader in {"Forge", "NeoForge"}:
        try: parsed = tomllib.loads(text)
        except tomllib.TOMLDecodeError: parsed = {}
        if isinstance(parsed, dict):
            for k in ["minecraft", "minecraft_version"]:
                if parsed.get(k): return _clean_version_string(parsed[k])
            deps = parsed.get("dependencies")
            if isinstance(deps, dict):
                for entries in deps.values():
                    for entry in (entries if isinstance(entries, list) else [entries]):
                        if isinstance(entry, dict) and entry.get("modId", "").lower() == "minecraft":
                            if entry.get("versionRange"): return _clean_version_string(entry["versionRange"])
            
        for pattern in [r'versionRange\s*=\s*"([^"]+)"', r'minecraft\s*=\s*"([^"]+)"', r'minecraft_version\s*=\s*"([^"]+)"']:
            match = re.search(pattern, text)
            if match: return _clean_version_string(match.group(1))
    return None

def extract_mod_metadata(loader: str, text: str) -> dict:
    result = {"mod_id": None, "mod_name": None, "mod_version": None, "description": None, "authors": [], "license": None, "homepage": None}
    if loader in {"Fabric", "Vanilla"}:
        try: data = json.loads(text)
        except: return result
        if loader == "Fabric":
            result.update({"mod_id": data.get("id"), "mod_name": data.get("name"), "mod_version": data.get("version"), "description": data.get("description"), "license": data.get("license")})
            authors = data.get("authors", [])
            result["authors"] = [a if isinstance(a, str) else a.get("name") for a in (authors if isinstance(authors, list) else [])]
            if isinstance(data.get("contact"), dict): result["homepage"] = data.get("contact").get("homepage")
    elif loader in {"Forge", "NeoForge"}:
        try: parsed = tomllib.loads(text)
        except: return result
        mods = parsed.get("mods", [])
        if isinstance(mods, list) and mods:
            m = mods[0] if isinstance(mods[0], dict) else {}
            result.update({"mod_id": m.get("modId"), "mod_name": m.get("displayName"), "mod_version": m.get("version"), "description": m.get("description", "").strip()})
            auths = m.get("authors", "")
            result["authors"] = [a.strip() for a in auths.split(",")] if isinstance(auths, str) else auths
        result.update({"license": parsed.get("license"), "homepage": parsed.get("displayURL") or parsed.get("issueTrackerURL")})
    return result

def compute_compatibility_status(current_version: Optional[str], target_version: str) -> str:
    if not current_version: return "Unknown Version"
    if current_version == target_version: return "Compatible"
    if current_version in SUPPORTED_VERSIONS: return "Needs Update"
    return "Unsupported Version"

def build_file_summary(archive_path: Path) -> dict:
    summary = {"total_files": 0, "java_files": 0, "class_files": 0, "json_files": 0, "toml_files": 0, "resource_files": 0, "texture_files": 0, "other_files": 0}
    try:
        with zipfile.ZipFile(archive_path, "r") as archive:
            for info in archive.infolist():
                if info.is_dir(): continue
                summary["total_files"] += 1
                ext = Path(info.filename.lower()).suffix
                if ext == ".java": summary["java_files"] += 1
                elif ext == ".class": summary["class_files"] += 1
                elif ext == ".json": summary["json_files"] += 1
                elif ext == ".toml": summary["toml_files"] += 1
                elif ext in {".png", ".jpg", ".jpeg", ".gif", ".tga", ".bmp"}: summary["texture_files"] += 1
                elif ext in {".mcmeta", ".properties", ".lang", ".txt", ".xml", ".cfg", ".yml", ".yaml", ".nbt"}: summary["resource_files"] += 1
                else: summary["other_files"] += 1
    except Exception: pass
    return summary

def declared_dependency_ids(loader: str, text: str) -> list[str]:
    rt = {"minecraft", "forge", "neoforge", "javafml", "fabricloader"}
    if loader == "Fabric":
        try: return sorted(k for k in json.loads(text).get("depends", {}) if k not in rt)
        except: return []
    if loader in {"Forge", "NeoForge"}:
        try:
            deps = tomllib.loads(text).get("dependencies", {})
            found = {entry.get("modId") or tid for tid, entries in (deps.items() if isinstance(deps, dict) else []) for entry in (entries if isinstance(entries, list) else [entries]) if isinstance(entry, dict)}
            return sorted(f for f in found if isinstance(f, str) and f not in rt)
        except: return []
    return []

def resolve_target_version(current_version: Optional[str], requested: Optional[str]):
    if requested and requested != "auto": return requested
    if not current_version: return SUPPORTED_VERSIONS[0]
    for version in SUPPORTED_VERSIONS:
        if version_tuple(version) <= version_tuple(current_version): return version
    return SUPPORTED_VERSIONS[-1]


# --- Asynchronous Worker Threads (The "BukkitRunnables") ---
def run_update_job(job_id: str, payload: UpdateRequest, stored: StoredFile):
    """Executes intensive AST mapping off the main event loop to prevent proxy timeouts."""
    try:
        target_mc = payload.target_version or "Unknown"
        inspector = ModInspector(stored.path, target_mc)
        inspection_results = inspector.inspect()
        
        stage = inspection_results["complexity_stage"]
        discovered_deps = inspection_results["discovered_dependencies"]
        loader = payload.loader if payload.loader and payload.loader != "auto" else inspection_results["loader"]

        target_version = resolve_target_version(inspection_results["current_version"], payload.target_version)
        dynamic_overrides = payload.dependency_overrides or {}
        
        # Run BetterDependency CLI to resolve third-party dependencies via Modrinth
        dep_analysis = run_betterdependency_cli(str(stored.path), target_version, loader)

        # Merge resolved dependency versions into the overrides.
        # User-provided overrides take priority over auto-resolved ones.
        resolved_overrides = extract_dependency_overrides(dep_analysis)
        merged_overrides = {**resolved_overrides, **dynamic_overrides}

        analyzer = ArchiveAnalyzer()
        analysis = analyzer.analyze_archive(stored.path, target_version=target_version)

        updated_archive_path = stored.path.with_name(f"{Path(stored.original_name).stem}-updated-{target_version}{stored.path.suffix}")

        engine = PortingEngine(target_version, loader)
        port_report = engine.apply_port(
            stored.path, updated_archive_path,
            custom_neoforge_version=payload.custom_neoforge_version,
            custom_javafml_version=payload.custom_javafml_version,
            custom_minecraft_version=payload.custom_minecraft_version,
            dependency_overrides=merged_overrides
        )

        token = uuid.uuid4().hex
        DOWNLOAD_FILES[token] = DownloadableFile(
            token=token, path=updated_archive_path, filename=updated_archive_path.name,
            media_type="application/java-archive" if stored.path.suffix.lower() == ".jar" else "application/zip"
        )

        # Collect unresolved dependency warnings for the report
        unresolved_deps = get_unresolved_dependencies(dep_analysis)

        # Mark ticket as resolved
        TASKS_STORE[job_id] = {
            "status": "completed",
            "filename": updated_archive_path.name,
            "size_bytes": updated_archive_path.stat().st_size,
            "download_url": f"/download/{token}",
            "changed_files": port_report.get("changed_files", []),
            "unresolved_issues": port_report.get("unresolved_issues", []),
            "bundled_dependencies_updated": port_report.get("bundled_dependencies_updated", []),
            "dependency_analysis": dep_analysis,
            "resolved_overrides": resolved_overrides,
            "unresolved_dependencies": unresolved_deps,
        }
    except Exception as exc:
        TASKS_STORE[job_id] = {"status": "failed", "error": str(exc)}


def run_batch_job(job_id: str, payload: BatchUpdateRequest, stored_files: list):
    """Executes batched multi-jar transformations securely off the main thread."""
    try:
        members = []
        for stored in stored_files:
            loader, meta_path = detect_loader_and_metadata(stored.path)
            with zipfile.ZipFile(stored.path, "r") as archive:
                text = archive.read(meta_path).decode("utf-8", errors="replace") if meta_path else ""
            c_ver = extract_current_version(loader, meta_path, text) if meta_path else None
            t_ver = resolve_target_version(c_ver, payload.target_version)
            
            out_path = stored.path.with_name(f"{stored.file_id}-{Path(stored.original_name).stem}-updated-{t_ver}{stored.path.suffix}")
            report = PortingEngine(t_ver, loader).apply_port(
                stored.path, out_path,
                custom_neoforge_version=payload.custom_neoforge_version,
                custom_javafml_version=payload.custom_javafml_version,
                custom_minecraft_version=payload.custom_minecraft_version,
                dependency_overrides=payload.dependency_overrides,
            )
            members.append({
                "file_id": stored.file_id, "output_path": out_path,
                "mod_id": extract_mod_metadata(loader, text)["mod_id"],
                "dependencies": declared_dependency_ids(loader, text),
                "changed_files": report["changed_files"],
                "unresolved_issues": report["unresolved_issues"],
                "bundled_dependencies_updated": report.get("bundled_dependencies_updated", [])
            })

        available_mods = {m["mod_id"] for m in members if m["mod_id"]}
        for m in members: m["missing_dependencies"] = [d for d in m["dependencies"] if d not in available_mods]

        bundle_path = STORAGE_DIR / f"minecraftporter-batch-{uuid.uuid4().hex}.zip"
        manifest = {"type": "minecraftporter-dependency-batch", "members": [{k: v for k, v in m.items() if k != "output_path"} for m in members]}
        
        with zipfile.ZipFile(bundle_path, "w", zipfile.ZIP_DEFLATED) as bundle:
            for m in members:
                bundle.write(m["output_path"], arcname=m["output_path"].name.split("-", 1)[-1])
            bundle.writestr("dependency-report.json", json.dumps(manifest, indent=2))

        token = uuid.uuid4().hex
        DOWNLOAD_FILES[token] = DownloadableFile(token, bundle_path, "minecraftporter-ported-mods.zip", "application/zip")
        
        TASKS_STORE[job_id] = {
            "status": "completed",
            "filename": "minecraftporter-ported-mods.zip",
            "size_bytes": bundle_path.stat().st_size,
            "download_url": f"/download/{token}",
            "changed_files": sorted({path for m in members for path in m["changed_files"]}),
            "unresolved_issues": sorted({issue for m in members for issue in m["unresolved_issues"]}),
            "missing_dependencies": sorted({dep for m in members for dep in m["missing_dependencies"]}),
            "bundled_dependencies_updated": sorted({note for m in members for note in m["bundled_dependencies_updated"]})
        }
    except Exception as exc:
        TASKS_STORE[job_id] = {"status": "failed", "error": str(exc)}


