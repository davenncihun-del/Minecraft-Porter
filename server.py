@app.post("/upload")
def upload_file(background_tasks: BackgroundTasks, file: UploadFile = File(...)):
    """Receives binary packet chunks safely, executing blocking I/O on the thread pool."""
    background_tasks.add_task(cleanup_old_files)

    if file.size and file.size > MAX_UPLOAD_SIZE:
        raise HTTPException(status_code=413, detail="Payload exceeds maximum allowed bounds.")

    safe_filename = sanitize_filename(file.filename or "file.bin")
    ext = Path(safe_filename).suffix.lower()
    if ext not in {".jar", ".zip"}:
        raise HTTPException(status_code=400, detail="Exploit blocked: Invalid file extension.")

    file_id = uuid.uuid4().hex
    destination = STORAGE_DIR / f"{file_id}{ext}"
    
    # C-level buffered I/O streaming to minimize JVM/Python heap pressure.
    try:
        with destination.open("wb") as buffer:
            shutil.copyfileobj(file.file, buffer)
    except Exception:
        raise HTTPException(status_code=500, detail="Fatal I/O fault while flushing to disk.")
    finally:
        file.file.close()

    stored = StoredFile(file_id=file_id, path=destination, original_name=safe_filename, size=destination.stat().st_size)
    STORED_FILES[file_id] = stored
    return {"file_id": stored.file_id, "filename": stored.original_name, "size_bytes": stored.size, "message": "Uploaded safely."}

@app.post("/analyze-dependencies")
def analyze_dependencies(payload: AnalyzeDependenciesRequest):
    stored = STORED_FILES.get(payload.file_id)
    if not stored: raise HTTPException(status_code=404, detail="File pointer invalid.")

    loader, metadata_path = detect_loader_and_metadata(stored.path)
    if payload.loader and payload.loader != "auto": loader = payload.loader

    with zipfile.ZipFile(stored.path, "r") as archive:
        current_text = archive.read(metadata_path).decode("utf-8", errors="replace") if metadata_path else ""
    current_version = extract_current_version(loader, metadata_path, current_text) if metadata_path else None
    target_version = resolve_target_version(current_version, payload.target_version)

    result = run_betterdependency_cli(str(stored.path), target_version, loader)
    if result.get("status") == "error":
        raise HTTPException(status_code=500, detail=result.get("message", "Analysis pipeline crashed"))
    return result

@app.post("/inspect")
def inspect_file(payload: InspectRequest):
    stored = STORED_FILES.get(payload.file_id)
    if not stored: raise HTTPException(status_code=404, detail="File pointer invalid.")

    target_mc = payload.minecraft_version or "Unknown"
    inspector = ModInspector(stored.path, target_mc)
    inspection_results = inspector.inspect()

    loader = inspection_results["loader"]
    metadata_path = inspection_results["metadata_path"]
    if not metadata_path: raise HTTPException(status_code=400, detail="Invalid mod structure detected.")

    with zipfile.ZipFile(stored.path, "r") as archive:
        mod_meta = extract_mod_metadata(loader, archive.read(metadata_path).decode("utf-8", errors="replace"))

    target_version = resolve_target_version(inspection_results["current_version"], payload.minecraft_version)
    
    return {
        "loader": loader,
        "current_version": inspection_results["current_version"],
        "target_version": target_version,
        "compatibility_status": compute_compatibility_status(inspection_results["current_version"], target_version),
        "complexity_stage": inspection_results["complexity_stage"],
        "update_reasons": inspection_results["update_reasons"],
        "discovered_dependencies": inspection_results["discovered_dependencies"],
        "has_source_code": inspection_results["has_source_code"],
        **mod_meta,
        "file_summary": build_file_summary(stored.path),
        "metadata_path": metadata_path
    }

# --- The Task Dispatchers (Non-Blocking) ---

@app.post("/update")
def update_file(payload: UpdateRequest, background_tasks: BackgroundTasks):
    """Instantly returns a job ticket, delegating AST rewrites to a worker thread."""
    stored = STORED_FILES.get(payload.file_id)
    if not stored: raise HTTPException(status_code=404, detail="File pointer invalid.")

    job_id = uuid.uuid4().hex
    TASKS_STORE[job_id] = {"status": "processing", "created_at": datetime.utcnow()}
    
    background_tasks.add_task(run_update_job, job_id, payload, stored)
    return {"job_id": job_id, "message": "Task delegated to worker pool."}

@app.post("/update-batch")
def update_batch(payload: BatchUpdateRequest, background_tasks: BackgroundTasks):
    unique_ids = list(dict.fromkeys(payload.file_ids))
    if len(unique_ids) < 2: raise HTTPException(status_code=400, detail="Batch execution requires 2+ valid archives.")
    
    stored_files = []
    for fid in unique_ids:
        stored = STORED_FILES.get(fid)
        if not stored: raise HTTPException(status_code=404, detail=f"File pointer {fid} invalid.")
        stored_files.append(stored)

    job_id = uuid.uuid4().hex
    TASKS_STORE[job_id] = {"status": "processing", "created_at": datetime.utcnow()}
    
    background_tasks.add_task(run_batch_job, job_id, payload, stored_files)
    return {"job_id": job_id, "message": "Batch task delegated to worker pool."}

